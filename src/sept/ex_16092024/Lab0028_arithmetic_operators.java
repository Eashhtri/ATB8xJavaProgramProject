package sept.ex_16092024;

public class Lab0028_arithmetic_operators {
    public static void main(String[] args) {

        //Arithmetic Operators
        // +,-,/,*

        int a =10;
        int b =20;

        System.out.println(a+b); // + ,- > Binary -- it requires two operands to work with
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);


        // % Modulus  -- Reminders

        System.out.println(a%b);

    }
}
